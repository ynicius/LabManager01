package DTO;

public class convenio_DTO {
	
	int IdConvenio;
	String nomeConvenio;
	String tabelaCobranca;
	int cobertura;
	
	
	
	public String getTabelaCobranca() {
		return tabelaCobranca;
	}
	public void setTabelaCobranca(String tabelaCobranca) {
		this.tabelaCobranca = tabelaCobranca;
	}
	public int getIdConvenio() {
		return IdConvenio;
	}
	public void setIdConvenio(int dConvenio) {
		this.IdConvenio = dConvenio;
	}
	public String getNomeConvenio() {
		return nomeConvenio;
	}
	public void setNomeConvenio(String nomeConvenio) {
		this.nomeConvenio = nomeConvenio;
	}
	public int getCobertura() {
		return cobertura;
	}
	public void setCobertura(int cobertura) {
		this.cobertura = cobertura;
	}
	
	
	
	

}
